/* Setzt eine CallerBox komplett auf 0 */
unsigned short BrbClearCallerBox(unsigned long pCallerBox, unsigned long nSize)
{
	memset((USINT*)pCallerBox, 0, nSize);
	return eBRB_ERR_OK;
}

